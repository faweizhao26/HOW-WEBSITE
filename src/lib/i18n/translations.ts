export type TranslationKey = keyof typeof common

export function t(
  key: string,
  locale: Locale,
  dict?: Record<string, Record<Locale, string>>
): string {
  // Try common first
  if (key in common) {
    return common[key as keyof typeof common][locale]
  }
  if (dict && key in dict) {
    return dict[key][locale]
  }
  return key
}
